def makeachoice():
    option = random.choice(data)
    return option

def checkAnswer(choice1, choice2):
    if choice1["follower_count"] >= choice2["follower_count"]:
        return True
    else:
        return False


def playGame():
    os.system('cls')
    print(logo)
    game_over = False
    score = 0
    option2 = makeachoice()

    while game_over == False:

        option1 = option2
        option2 = makeachoice()
        while option1 == option2:
            option2 = makeachoice()

        print(f'Option A: {option1["name"]}, a {option1["description"]} from {option1["country"]}. Number of followers: {option1["follower_count"]}m.')
        print(vs)
        print(f'Option B: {option2["name"]}, a {option2["description"]} from {option2["country"]}.')

        answer = input("Who has more followers? 'A' or 'B': ")

        if answer == "A":
            flag = checkAnswer(option1, option2)
            if flag == True:
                os.system('cls')
                print(logo)
                score += 1
                print(f"Correct. {option1['name']} has {option1['follower_count']}m followers, while {option2['name']} has {option2['follower_count']}m followers. Your current score: {score}.\n")
            else:
                game_over = True
        elif answer == "B":
            flag = checkAnswer(option2, option1)
            if flag == True:
                os.system('cls')
                print(logo)
                score += 1
                print(f"Correct. {option1['name']} has {option1['follower_count']}m followers, while {option2['name']} has {option2['follower_count']}m followers. Your current score: {score}.\n")
            else:
                game_over = True
        temp = option1
        option1 = option2        
    
    print(f"\nIncorrect. {temp['name']} has {temp['follower_count']}m followers, while {option2['name']} has {option2['follower_count']}m followers. your final score is {score}.")
        


to_cont = "y"
